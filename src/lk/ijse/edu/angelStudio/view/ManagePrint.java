    private void looodAllPrint() {
        try {
            ArrayList<Print> allPrint = ManagePrintController.getAll();
            DefaultTableModel dtm = (DefaultTableModel) tblPrint.getModel();
            dtm.setRowCount(0);
            for (Print print : allPrint) {
                Object[] rowData = {
                    print.getPrid(),
                    print.getName(),
                    print.getType(),
                    print.getSize(),
                    print.getPrice(),
                    print.getQty()
                
                };
                
                dtm.addRow(rowData);
                
            }
        } catch (Exception ex) {
            Logger.getLogger(ManagePrint.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

