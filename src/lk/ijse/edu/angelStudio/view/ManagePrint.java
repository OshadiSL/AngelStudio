    private void lblPrintRemoveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblPrintRemoveMouseClicked
        try {
            if (tblPrint.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(null, "Select A Row");
            }
            boolean isDelect = ManagePrintController.delete(tblPrint.getValueAt(tblPrint.getSelectedRow(), 0).toString());
            if (isDelect) {
                JOptionPane.showMessageDialog(null, "Delete A Row");
                looodAllPrint();
                txt();
            }
        } catch (Exception ex) {
            Logger.getLogger(ManagePrint.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lblPrintRemoveMouseClicked

    private void lblPrintSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblPrintSaveMouseClicked
        try {
            String id = txtPrintID.getText();
            String name = txtPrintName.getText();
            String yrpe = txtPrintType.getText();
            String size = txtPrintSize.getText();
            double price = Double.parseDouble(txtPrintPrice.getText());
            int qty = Integer.parseInt(txtPrintQty.getText());
            PrintDTO print = new PrintDTO(id, name, yrpe, size, price, qty);
            boolean isAdded = ManagePrintController.add(print);
            if (isAdded) {
                JOptionPane.showMessageDialog(null, "Add NEdw Print");
                looodAllPrint();
                txt();
            }
        } catch (Exception ex) {
            Logger.getLogger(ManagePrint.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lblPrintSaveMouseClicked

